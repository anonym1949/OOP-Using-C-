using System;

namespace MathLibrary
{
    public class ClassLibrary
    {
        public int Sum(int N1, int N2)
        {
            return N1 + N2;
        }

        public int Mul(int N1, int N2)
        {
            return N1 * N2;
        }

        public int Sub(int N1, int N2)
        {
            return N1 - N2;
        }

    }

}